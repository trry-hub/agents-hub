  function resolveLocale(rawLocale) {
    return String(rawLocale || '').toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
  }

  function format(message, values) {
    return Object.keys(values || {}).reduce((result, key) => {
      return result.replace(new RegExp(`\\{${key}\\}`, 'g'), () => values[key]);
    }, message);
  }

  window.AssistantI18n = {
    locale: resolveLocale(document.body.dataset.locale),
    t(key, values) {
      const dictionary = messages[this.locale] || messages.en;
      return format(dictionary[key] || messages.en[key] || key, values || {});
    },
    apply(root) {
      const target = root || document;
      target.querySelectorAll('[data-i18n]').forEach((node) => {
        node.textContent = this.t(node.dataset.i18n);
      });
      target.querySelectorAll('[data-i18n-title]').forEach((node) => {
        const value = this.t(node.dataset.i18nTitle);
        node.title = value;
        node.setAttribute('aria-label', value);
      });
      target.querySelectorAll('[data-i18n-aria]').forEach((node) => {
        node.setAttribute('aria-label', this.t(node.dataset.i18nAria));
      });
      target.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
        node.placeholder = this.t(node.dataset.i18nPlaceholder);
      });
    },
  };
